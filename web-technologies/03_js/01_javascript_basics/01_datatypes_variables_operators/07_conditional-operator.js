let number1 = 100;
let number2 = 10;

//operator trójargumentowy
//Jeżeli number1 jest większy od number2 to mamy true i do biggerNumber przypisujemy number1
//jeśli number1 jest mniejsze od number2 to mamy false i do biggerNumber przypisujemy number2;
let biggerNumber = number1 > number2 ? number1 : number2;

console.log("Wynik działania operatora trójargumentowego: " + biggerNumber);